    <h1 id=\"indian-penal-code-1860-amended-upto-2019\"><strong>INDIAN PENAL CODE, 1860 [AMENDED UPTO 2019]</strong>
    </h1>
    <p><strong>[Act No. 45 of 1860]</strong><a href=\"#fn1\" class=\"footnote-ref\" id=\"fnref1\"><sup>1</sup></a></p>
    <p><strong>[06<sup>th</sup> October, 1860]</strong></p>
    <h2 id=\"preamble\"><strong>PREAMBLE</strong></h2>
    <p>WHEREAS it is expedient to provide a general Penal Code for <a href=\"#fn2\" class=\"footnote-ref\"
            id=\"fnref2\"><sup>2</sup></a>[India]<em>;</em></p>
    <p>It is enacted as follows.</p>
    <p><strong>Chapter 1 INTRODUCTION</strong></p><br>
    <div id='0'>
        <h4 data-label='amusoftech' class='font-weight-bold mb-3'>Section 1 - Title and extent of operation of the Code
        </h4></br>
        <p>
        <p>This Act shall be called the Indian Penal Code, and shall <a href=\"#fn3\" class=\"footnote-ref\"
                id=\"fnref3\"><sup>3</sup></a>[extend to the whole of India <a href=\"#fn4\" class=\"footnote-ref\"
                id=\"fnref4\"><sup>4</sup></a>[<a href=\"#fn5\" class=\"footnote-ref\"
                id=\"fnref5\"><sup>5</sup></a>[***].]</p>
        </p>
    </div>
    <div id='1'>
        <h4 data-label='amusoftech' class='font-weight-bold mb-3'>Section 2 - Punishment of offences committed within
            India</h4></br>
        <p>
        <p>Every person shall be liable to punishment under this Code and not otherwise for every act or omission
            contrary to the provisions thereof of which, he shall be guilty within <a href=\"#fn6\"
                class=\"footnote-ref\" id=\"fnref6\"><sup>6</sup></a>[India] <a href=\"#fn7\" class=\"footnote-ref\"
                id=\"fnref7\"><sup>7</sup></a>[***].</p>
        </p>
    </div>
    <div id='2'>
        <h4 data-label='amusoftech' class='font-weight-bold mb-3'>Section 3 - Punishment of offences committed beyond but
            which by law may be tried within India</h4></br>
        <p>
        <p>Any person liable, by any <a href=\"#fn8\" class=\"footnote-ref\" id=\"fnref8\"><sup>8</sup></a>[Indian law]
            to be tried for an offence committed beyond <a href=\"#fn9\" class=\"footnote-ref\"
                id=\"fnref9\"><sup>9</sup></a>[India] shall be dealt with according to the provisions of this Code for
            any act committed beyond <a href=\"#fn10\" class=\"footnote-ref\" id=\"fnref10\"><sup>10</sup></a>[India] in
            the same manner as if such act had been committed within <a href=\"#fn11\" class=\"footnote-ref\"
                id=\"fnref11\"><sup>11</sup></a>[India].</p>
        </p>
    </div>
    
   
    <div id='574'>
        <h4 data-label='amusoftech' class='font-weight-bold mb-3'>Section 511 - Punishment for attempting to commit
            offences punishable with imprisonment for life or other imprisonment </h4></br>
        <p>
        <p>Whoever attempts to commit an offence punishable by this Code with <a href=\"#fn491\" class=\"footnote-ref\"
                id=\"fnref491\"><sup>491</sup></a>[imprisonment for life] or imprisonment, or to cause such an offence
            to be committed, and in such attempt does any act towards the commission of the offence, shall, where no
            express provision is made by this Code for the punishment of such attempt, be punished with <a
                href=\"#fn492\" class=\"footnote-ref\" id=\"fnref492\"><sup>492</sup></a>[imprisonment of any
            description provided for the offence, for a term which may extend to one-half of the imprisonment for life
            or, as the case may be, one-half of the longest term of imprisonment provided for that offence], or with
            such fine as is provided for the offence, or with both.</p>
        <p><strong>Illustrations</strong></p>
        <ol type=\"a\">\n<li>
                <p>A makes an attempt to steal some jewels by breaking open a box, and finds after so opening the box,
                    that there is no jewel in it. He has done an act towards the commission of theft, and therefore is
                    guilty under this section.</p>
            </li>\n<li>
                <p>A makes an attempt to pick the pocket of Z by thrusting hi s hand into Z's pocket. A fails in the
                    attempt in consequence of Z's having nothing in hi s pocket. A is guilty under this section.</p>
            </li>\n</ol>
